#Program untuk menjumlahkan, mengurangi, dan mengalikan dua bilangan yang dimasukkan oleh user

num1 = int(input("Masukkan bilangan bulat pertama: "))
num2 = int(input("Masukkan bilangan bulat kedua: "))

penjumlahan = num1 + num2
pengurangan = num1 - num2
pengalian = num1 * num2

print("Hasil penjumlahan kedua bilangan tersebut adalah", penjumlahan)
print("Hasil pengurangan kedua bilangan tersebut adalah", pengurangan)
print("Hasil pengalian kedua bilangan tersebut adalah", pengalian)